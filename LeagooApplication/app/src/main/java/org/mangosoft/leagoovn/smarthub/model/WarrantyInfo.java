public class WarrantyInfo {
    public String bodyExpiredDate;
    public String acceExpiredDate;
    public String phoneNumber;

    public String getBodyExpiredDate() {
        return bodyExpiredDate;
    }

    public void setBodyExpiredDate(String bodyExpiredDate) {
        this.bodyExpiredDate = bodyExpiredDate;
    }

    public String getAcceExpiredDate() {
        return acceExpiredDate;
    }

    public void setAcceExpiredDate(String acceExpiredDate) {
        this.acceExpiredDate = acceExpiredDate;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
}
