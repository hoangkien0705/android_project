package org.mangosoft.leagoovn.smarthub.model;

import org.mangosoft.leagoovn.smarthub.components.EventBase;

/**
 * Created by thanghx on 4/8/16.
 */
public class SMSWarrantyEvent extends EventBase {

    private boolean success;

    private String smsContent, smsCenter, from;

    public String getSmsContent() {
        return smsContent;
    }

    public void setSmsContent(String smsContent) {
        this.smsContent = smsContent;
    }

    public String getSmsCenter() {
        return smsCenter;
    }

    public void setSmsCenter(String smsCenter) {
        this.smsCenter = smsCenter;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }
}
